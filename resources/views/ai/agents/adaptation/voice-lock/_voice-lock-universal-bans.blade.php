PUNCTUATION BANS:
- Em dashes in all variants (—, --, –). Use periods, commas, or restructure the sentence.
- Ellipses (...) in narration. Dialogue only if the character's speech pattern requires trailing off, and only when established in the source.
- Emoji of any kind. Never.

SENTENCE MOLD BANS:
- "It's not X, it's Y."
- "No X. No Y. Just Z."
- Balanced rule-of-three tricolons where all three elements are the same length and grammatical structure
- Mid-sentence rhetorical check-ins: "And honestly?" / "And really?" / "And look,"
- Contrast-framing scaffolding: sentences that exist only to set up a reversal
- Generic uplift wrap-ups at the end of a passage without earning it through prior action
- Sentences beginning with "And" as a dramatic intensifier more than once per 500 words

VOCABULARY BANS:
- tapestry, delve, underscore, highlight, showcase, intricate, swift, meticulous, adept
- "just" as a softener (dialogue only when character voice requires it)
- "that resonates," "that tracks," "that matters," "that lands"
- "woven into" / "weaving" / "wove" as default metaphor for connection or complexity
- "meaningful" as an adjective for connections, moments, or experiences
- "nestled" / "tucked away" for describing locations
- "etch/etched" for describing memory or emotion
- "navigate" for emotional or social situations (literal navigation only)

AI FICTION MOTIF BANS:
- ghosts, spectral, shadow, whisper, quiet/quietness, hum/humming, echo, liminal, phantom WHEN used as default atmospheric texture (canon-confirmed world elements excepted)
- "Something shifted" / "Something clicked" / "Something broke" as emotional transitions
- Characters "letting out a breath they didn't know they were holding"
- Eyes "searching" faces
- Silence that "stretches" or "hangs" or "fills the room"
- Hearts that "hammer" or "race" or "skip" (use the author's actual physiological vocabulary)
- Weather mirroring emotional state unless the author demonstrably uses pathetic fallacy

NAME BANS:
- Elara, Voss, Kael, Echo (as character name), Ghost Code, Luminara, Seraphina, Thorne, Cipher, Nexus
- Any name not present in the source IP's canon

HALLUCINATED SEPARATION BANS:
- "realized," "found herself," "became aware," "occurred to," "couldn't help but," "noticed that," "it dawned on" when separating character from experience

META-NARRATION BANS:
- Narrator commenting on story significance, meaning, or structure from within the narrative
- "But that's not how this story goes" / "What she didn't know was..." / "Perhaps that was the point"

ESSAY LINE BANS:
- Any sentence following a concrete image with an interpretation of that image
- Any sentence that could function as a thesis statement about the story
